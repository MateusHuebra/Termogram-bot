    }

    static function hide(string $text) {
        $text = str_replace(self::WRONG, '🔴', $text); 
        $text = str_replace(self::DISPLACED, '🟡', $text); 
        $text = str_replace(self::RIGHT, '🟢', $text);
        return str_replace(' ', '', $text);
    }

